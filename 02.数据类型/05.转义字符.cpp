#include <iostream>
using namespace std;

int main() {

  // 转义字符
  
  // 换行符 \n
  cout << "hello word \n";

  // 反斜杠 \\

  cout << "hello word \\" << endl;

  // 水平制表符 \t 作用是整理输出数据
  cout << "aa\t\t\t hello word" << endl;
  cout << "aaaaa\t\t hello word" << endl;
  cout << "aaa\t\t\t hello word" << endl;

  return 0;
}